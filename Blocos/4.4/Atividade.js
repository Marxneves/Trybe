// Atividade para transfomrar os primeiros exercicios em função.
// Adição
// Subtracão
// Multiplicação
// Divisão
// Módulo

function soma(a,b){
  return a+b;
}

function subtracao(a,b){
  return a-b;
}

function multiplicacao(a,b){
  return a*b;
}

function divisao(a,b){
  return a/b;
}

function modulo(a,b){
  return a%b;
}

console.log(soma(1,4));
console.log(subtracao(5,2));
console.log(multiplicacao(2,4));
console.log(divisao(6,2));
console.log(modulo(16,2));

